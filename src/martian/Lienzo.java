            //celda de arriba
            EvaluarAdyacente(filaEntrada_ - 1, columnaEntrada_, actual);
            //celda de la derecha
            EvaluarAdyacente(filaEntrada_, columnaEntrada_ + 1, actual);
            //celda de abajo
